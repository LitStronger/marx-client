<template>
  <div class="singleMulti">
    <h3>{{title}}</h3>
    <a-list :dataSource="choicesList">
      <a-list-item slot="renderItem" slot-scope="item">
        <div :class="item.tag === select ? 'select' : ''">{{item.tag}}、{{item.content}}</div>
      </a-list-item>
    </a-list>

    <p>正确答案： {{right}}</p>
  </div>
</template>

<script>
import { Radio } from "ant-design-vue";

export default {
  name: "SingleMulti",
  props: {
    title: String,
    choices: Object,
    right: String,
    select: String
  },
  created() {
    this.choicesList = Object.keys(this.choices).map(tag => ({
      tag,
      content: this.choices[tag]
    }));
  },
  data() {
    return {
      value: ""
    };
  },
  components: {
    aRadio: Radio,
    aRadioGroup: Radio.Group
  },
  methods: {
    onChange: function() {
      this.$emit("update", this.value);
    }
  },
  model: {
    prop: "answer", //绑定的值，通过父组件传递
    event: "update" //自定义时间名
  }
};
</script>

